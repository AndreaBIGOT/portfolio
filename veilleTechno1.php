                <div class="solution soluPerso">
                    <h3>Actions quotidiennes</h3>

                    <ul>
                        <li>- Avoir une bonne position</li>
                        <li>- Faire des étirements régulièrement</li>
                        <li>- Adapter son bureau</li>
                        <li>- Faire une activité physique</li>
                        <li>- Manger équilibré</li>
                        <li>- Avoir 7/8h de sommeil par nuit</li>
                        <li>- Respecter la règle 20-20-20: prendre 20 secondes de pause <br> en regardant à 20 pieds toutes les 20 minutes</li>
                        <li>- Eviter les écrans au moins 2h avant le coucher</li>
                    </ul>
                </div>

                <div class="solution soluPro">
                    <h3>Le rôle des entreprises</h3>

